# pedirle un numero al usuario

numero = input("¿Cuál es tu número favorito? ")
numero = int(numero)  # Convertir el string a un número entero
print(f"Tu número favorito es: {numero}")
numero = input("¿Cuál es tu número favorito como decimal? ")
numero = float(numero)  # Convertir el número entero a un número decimal (float)
print(f"Tu número favorito como decimal es: {numero}")